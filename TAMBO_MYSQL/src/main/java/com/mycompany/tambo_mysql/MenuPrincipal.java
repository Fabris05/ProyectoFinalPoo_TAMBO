    public void CalcularPrecio(){
        
        String Codigo=this.jcbxCodigos.getSelectedItem().toString();
        cantidad=Integer.parseInt(this.jspCantidad.getValue().toString());
        PreparedStatement ps=null;
        ResultSet rs=null;
        Conexion cx=new Conexion();
        Connection con=cx.getConexion();
        
        try{
            String sql="SELECT Precio, Producto FROM PRODUCTO where Codigo='"+Codigo+"'";
            ps=con.prepareStatement(sql);
            rs=ps.executeQuery();
            rs.next();
            
            String producto=rs.getString("Producto");
            jlbProducto.setText(producto);
            precio=Double.parseDouble(rs.getString("Precio"));
            jblPrecio.setText(aMoneda(precio));
            jblImporte.setText(aMoneda(Math.round(precio * cantidad * 100.0) / 100.0));
            
        }catch(SQLException ex){
            System.err.println(ex.toString());
        }
    }
    
