    public void ActualizarTabla(){
        
        //Creamos la tabla CARRITO DE COMPRAS
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Producto");
        modelo.addColumn("Precio");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Importe");
        
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        double subTotal=0;
        
        
        //Mostramos los atributos de la tabla Carrito y se calculan los
        //diferentes importes.
        for(Productos c:ListaVentas){ 
            Object x[]=new  Object[5];
            x[0]=c.getCodigo();
            x[1]=c.getProducto();
            x[2]=c.getPrecio();
            x[3]=c.getCantidad();
            x[4]=c.getImporte();
            subTotal+=c.getImporte();
            
            jtblTablaCarrito.getColumnModel().getColumn(0).setPreferredWidth(20);
            modelo.addRow(x);
        }
        
        double igv=Math.round(subTotal*0.18);
        
        double total=subTotal+igv;
        
        jlbSubtotal.setText(aMoneda(subTotal));
        jlbIgv.setText(aMoneda(igv));
        jlbTotal.setText(aMoneda(total));
        jtblTablaCarrito.setModel(modelo);
        
        jlbResumenSubtotal.setText(aMoneda(subTotal));
        jlbResumenIGV.setText(aMoneda(igv));
        jlbResumenTotal.setText(aMoneda(total));
    }
